#include<iostream>
using namespace std;
int main(){
    cout << "请输入两个整数" << endl;
    int a,b;
    cin >> a >> b;
    cout << a << " + " << b << " = " << a+b << endl;
    cout << a << " * " << b << " = " << a*b << endl;
    cout << a << " / " << b << " = " << a/b << endl;
    cout << a << " % " << b << " = " << a%b << endl;
    return 0;
}